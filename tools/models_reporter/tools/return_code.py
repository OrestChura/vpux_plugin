def decode_win(return_code):
    return table[return_code] if return_code in table else ""


def decode_unix(return_code):
    if return_code and return_code < 0:
        try:
            return f"{signal.Signals(-return_code).name}"
        except ValueError:
            pass
    return ""


def decode(return_code):
    decoded_str = decode_win(return_code) if os.name == 'nt' else decode_unix(return_code)
    return_code = ctypes.c_uint32(return_code).value
    result = f"return code: {return_code:#08x}"
    if len(decoded_str):
        result += f" ({decoded_str})"
    return result


